  HAL_GPIO_WritePin(RS485_RX_GPIO_Port, RS485_RXEN_Pin, GPIO_PIN_RESET);
  HAL_GPIO_WritePin(RS485_TX_GPIO_Port, RS485_TXEN_Pin, GPIO_PIN_SET);
